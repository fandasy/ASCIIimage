type Chars [256]byte

// NewChars accepts only ascii characters, in the format: dark ... light
//
// Default: "@%#*+=:~-.  "
func NewChars(chars string) *Chars {
	if len(chars) == 0 {
		return DefaultChars()
	}

	// Only ascii chars
	if len(chars) != len([]rune(chars)) {
		return DefaultChars()
	}

	bytes := Chars{}

	charsLen := len(chars)
	for brightness := 0; brightness < 256; brightness++ {
		idx := brightness * (charsLen - 1) / 255
		bytes[brightness] = chars[idx]
	}

	return &bytes
}

// DefaultChars set defaultChars = "@%#*+=:~-.  "
func DefaultChars() *Chars {
	return defaultChars
}

